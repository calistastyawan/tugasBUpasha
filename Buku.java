public class Buku {
    String Judul;
    int id;
    String Pengarang;
    int TahunTerbit;

    //Constructor Parameter
    public Buku(String Judul, int id, String Pengarang, int TahunTerbit) {
        this.Judul = Judul;
        this.id = id;
        this.Pengarang = Pengarang;
        this.TahunTerbit = TahunTerbit;
    }
}
    

